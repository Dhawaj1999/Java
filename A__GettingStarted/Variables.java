package A__GettingStarted;

public class Variables {
    public static void main(String[] args) {
        // maths calculations using variables
        int i = 1;
        int j = 4;
        int k = 6;
        int m = (i+j) - k;
        int n = j*m;
        System.out.println(m);
        System.out.println(n);
        System.out.println((n/m)+k+m);
    }
}
