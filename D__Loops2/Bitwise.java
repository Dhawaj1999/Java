package D__Loops2;

public class Bitwise {
    public static void main(String[] args) {
        // AND
        System.out.println(2 & 3);
        // OR
        System.out.println(2 | 3);
        // Negation
        System.out.println(~ 2);
        // XOR
        System.out.println(2 ^ 3);
        // Left shift by 1
        System.out.println(34 << 1);
        // Right shift by 1
        System.out.println(34 >> 1);

    }
}
